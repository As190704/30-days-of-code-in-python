# Q1 >> Using + with spaces
result = 'Thirty' + ' ' + 'Days' + ' ' + 'Of' + ' ' + 'Python'
print(result)  # Output: Thirty Days Of Python

# Or using join (cleaner way)
result = ' '.join(['Thirty', 'Days', 'Of', 'Python'])
print(result)  # Output: Thirty Days Of Python

